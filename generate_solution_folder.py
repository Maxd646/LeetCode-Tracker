import os

def slugify(title):
    return title.strip().lower().replace(" ", "-")

def create_leetcode_problem_folder(problem_number, title, solution_code):
    folder_name = f"{problem_number}-{slugify(title)}"
    base_path = os.path.join("leetcode-solutions", folder_name)
    os.makedirs(base_path, exist_ok=True)

    # Write solution.py
    with open(os.path.join(base_path, "solution.py"), "w", encoding="utf-8") as f:
        f.write(f"# Leetcode {problem_number}: {title}\n")
        f.write(f"# https://leetcode.com/problems/{slugify(title)}/\n\n")
        f.write(solution_code.strip() + "\n")

    # Write README.md
    with open(os.path.join(base_path, "README.md"), "w", encoding="utf-8") as f:
        f.write(f"# Leetcode {problem_number} - {title}\n\n")
        f.write(f"[ðŸ”— Problem Link](https://leetcode.com/problems/{slugify(title)}/)\n\n")
        f.write("## Description\n\n Time Complexity: O(n*m)\nSpace Complexity: O(n) \n\n")
        f.write("## Solution\n\nSee [`solution.py`](solution.py)\n")

    print(f"[âœ…] Created folder and files at: {base_path}")

if __name__ == "__main__":
    problem_number = "pro 1427 "
    problem_title = "Perform String Shifts"
    solution_code = '''
class Solution:
    def stringShift(self, s: str, shift: list[list[int]]) -> str:
        for i in range(len(shift)):
            if shift[i][0] ==0:
                s=s[shift[i][1]:]+s[:shift[i][1]]
            elif shift[i][0]==1:
                s=s[-shift[i][1]:]+s[:-shift[i][1]]
        return s
'''
    create_leetcode_problem_folder(problem_number, problem_title, solution_code)
